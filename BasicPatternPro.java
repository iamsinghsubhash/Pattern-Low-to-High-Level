// Basic Pattern programs 

public class BasicPatternPro{
    public static void main(String args[]){
        int i,j;
          //outer loop
        for(j=0;j<=5;j++){
            //inner loop
           for(i=0;i<=5;i++){
             System.out.print("*");
           }
        System.out.println("*");
        }
    }
}